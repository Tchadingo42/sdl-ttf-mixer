#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*temp;

	temp = b;
	i = -1;
	while (++i < len)
		temp[i] = (unsigned char)c;
	return (b);
}
